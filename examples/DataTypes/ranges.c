#include <stdio.h>
#include <limits.h>
#include <stdint.h>
#include <stdlib.h>
void ranges()
{
	printf("The minimum value of a char is %d \n", CHAR_MIN);
	printf("The maximum value of a char is %d \n", CHAR_MAX);
	printf("The minimum value of a int is %d \n", INT_MIN);
	printf("The maximum value of a int is %d \n", INT_MAX);
	printf("The maximum value of an unsigned int is %u \n", UINT_MAX);
	printf("The maximum value of a long is %ld \n", LONG_MAX);
	printf("The maximum value of a size_t is %zu \n", SIZE_MAX); // defined in <stdint.h>



}




